num1 = 5
num2 = 3.5

print(num1 + num2) # Som
print(num1 - num2) # Sub
print(num1 * num2) # Mult
print(num1 / num2) # Div
print(num1 % num2) # Resto da Div
print(num1 ** num2) # Exponenciação
print(num1 // num2) # Floor Div - Arrendonda para o num inteiro mais próximo.

#Funções

print(abs(-15)) #Retorna o número absoluto.
print(pow(3,3)) #Exponenciação.
print(max(1,5)) #Retorna o número máximo.
print(min(1,5)) #Retorna o número mínimo.
print(round(8.9)) #Arredonda o numéro para o mais próximo. Ex: 8.3 = 8 / 8.9 = 9.

print(math.floor(8.8)) #Arredonda sempre para baixo.
print(math.ceil(8.1)) #Arredonda sempre para cima.
print(math.sqrt(10)) #Retorna a raiz quadrada de 8.
